# For Loop with Lists

list = [1,2,3,4,5,"Aman","Rohan",23.22,True,7,8,9,"avanish","rohit","alfredo"]
for i in list:
    print(i)


# For Loop with Tuples

tuple = ("Aman",2,3,4,45.56,True,"harry","john","alish","jassi")
for i in tuple:
    print(i)


# For Loop with Strings

string = "Uditya"
for i in string:
    print(i)